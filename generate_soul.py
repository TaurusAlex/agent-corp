def get_industry_keyword(filepath):
    """从文件路径提取行业关键词"""
    path = filepath.lower()
    
    # 优先匹配更具体的关键词
    keywords = sorted(INDUSTRY_KEYWORDS.keys(), key=len, reverse=True)
    for kw in keywords:
        if kw.lower() in path:
            return kw
    return "通用"

def get_role_type(filename):
    """从文件名提取角色类型"""
    name = filename.replace(".md", "")
    
    # 优先匹配更长的角色类型
    roles = sorted(ROLE_TYPES.keys(), key=len, reverse=True)
    for role in roles:
        if role in name:
            return role, ROLE_TYPES[role]
    
    return "顾问", {"desc": "提供专业建议", "style": "专业实用"}

def get_dont_list(filepath):
    """获取绝对不要列表"""
    path = filepath.lower()
    
    for key in ["农业", "畜牧", "渔业", "医疗", "金融", "法律", "投资"]:
        if key in path:
            return DONT_LIST.get(key, DONT_LIST["default"])
    
    return DONT_LIST["default"]

def generate_soul(filepath, role_name, industry):
    """生成SOUL.md内容"""
    role_type, role_info = get_role_type(os.path.basename(filepath))
    expertise = INDUSTRY_KEYWORDS.get(industry, "本行业相关知识")
    donts = get_dont_list(filepath)
    
    content = f"""# {role_name}

你是{role_name}，{role_info['desc']}。
{role_info['style']}。说话实在，不整虚的。

## 风格
- 直接给答案，不绕弯子
- 实用为主，少讲理论
- 不确定的说"这个要看你具体情况"

## 专长
- 深度：{expertise}
- 中等：相关联的业务知识
- 不涉及：跨行业专业服务

## 原则
- 推荐成熟方案，不追新冒进
- 关注风险，丑话说在前面

## 绝对不要
- {donts[0]}
- {donts[1]}
- {donts[2] if len(donts) > 2 else donts[0]}

## 用户
- 行业从业者、创业者、需要专业帮助的人
- 关心实操、效果、成本

## 常见问题
- "能不能做"——先评估你自身条件
- "要多少钱"——看你规模和具体需求
- "多久见效"——行业不同周期不同
"""
    
    # 控制字数
    lines = content.split('\n')
    total_chars = sum(len(line) for line in lines)
    
    return content

